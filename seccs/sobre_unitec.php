		<h1>¿Qué es Unitec?</h1>
		<p>
			Las siglas Unitec provienen de Unidad de Investigación y Desarrollo para la Calidad de la Educación en Ingeniería con orientación al uso de TIC Área Departamental Electrotecnia - Facultad de Ingeniería - UNLP
		</p>
		<h2>Objetivos:</h2>
			<ul>
				<li>
					<p>
					Impulsar la aplicación de Normas de Calidad en la Educación en áreas tecnológicas y promover la Gestión de la Calidad Educativa, y
					Impulsar la aplicación de Normas de Calidad en la Educación en áreas tecnológicas y promover la Gestión de la Calidad Educativa.
					</p>
				</li>
				<li>
					<p>
					Abordar cuestiones sobre la posible aplicación actual de las TICS para mejorar la calidad de la actividad docente, su complementación con los medios didácticos tradicionales, no solo para un mayor aprovechamiento del tiempo, sino para una mejora integral de la enseñanza, que contribuya a formar profesionales eficientes y con alto sentido de la responsabilidad. 
					</p>
				</li>
			</ul>
		<h2>Metodología para el desarrollo de los objetivos:</h2>
			<ul>
				<li>
					<p>  Llevar adelante tareas de investigación en el área de innovación y mejoramiento de la Calidad en la Educación y entrenamiento en Ingeniería (Educación basada en competencias, evaluación de aprendizajes, barreras al aprendizaje, etc), incorporando las herramientas de las Tecnologías de la Información y la Comunicación (TIC). <br>
					<br>
						 Promover desarrollos tecnológicos y asesoramientos a requerimiento de terceros e impulsar tareas de investigación, extensión, asesoramiento y capacitación, basados en los objetivos propuestos.<br>
					<br>
						 Promover y realizar estudios sobre indicadores para el mejoramiento continuo de la Educación en Ingeniería.<br>
					<br>
						 Fomentar actividades de transferencia de conocimientos, a través de la capacitación y formación de los miembros de la UID en temas relacionados con las TIC aplicadas a la Educación, otras Tecnologías y al Aseguramiento de la Calidad.  
					</p>
				</li>
			</ul>
